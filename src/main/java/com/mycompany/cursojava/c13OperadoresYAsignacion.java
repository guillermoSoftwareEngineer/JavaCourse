public class c13OperadoresYAsignacion {
     public static void main(String[] args) {
        
        
        //##################### SUMA #################################
        
        int numero = 20;
        //numero = numero + 15; se puede declarar la linea asi
        numero += 15; // produce el mismo resultado es como un shortcut
        System.out.println(numero);
        
        //##################### RESTA #################################
        
        int numeroRest = 20;
        //numeroRst = numeroRest + 15; se puede declarar la linea asi
        numeroRest -= 10; // produce el mismo resultado es como un shortcut
        System.out.println(numeroRest);
        
        //##################### MULTIPLICACION #################################
        
        int numeroMult = 20;
        //numeroMult = numeroMult + 15; se puede declarar la linea asi
        numeroMult *= 2; // produce el mismo resultado es como un shortcut
        System.out.println(numeroMult);
        
        //##################### DIVISION #################################
        
        int numeroDiv = 20;
        //numeroDiv = numeroDiv + 15; se puede declarar la linea asi
        numeroDiv *= 4; // produce el mismo resultado es como un shortcut
        System.out.println(numeroDiv);
        
        //##################### MODULO #################################
        
        int numeroMod = 20;
        //numeroMod = numeroMod + 15; se puede declarar la linea asi
        numeroMod *= 2; // produce el mismo resultado es como un shortcut
        System.out.println(numeroMod);
    }
}
